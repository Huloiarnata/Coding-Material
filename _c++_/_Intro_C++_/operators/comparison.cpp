#include<iostream>
using namespace std ;
int main(){
    int a=7, b=10 ;
    cout<<"equal to : "<<(a==b)<<endl;
    cout<<"not equal to : "<<(a!=b)<<endl;
    cout<<"greater then : "<<(a>b)<<endl;
    cout<<"less then : "<<(a<b)<<endl;
    cout<<"greater then equal to : "<<(a>=b)<<endl;
    cout<<"less then equal to : "<<(a<=b)<<endl;

}