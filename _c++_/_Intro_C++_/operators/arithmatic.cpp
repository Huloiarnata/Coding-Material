#include <iostream>
using namespace std;
int main(){
    int x=8,y=3 ;
    cout<<"for addition : "<<x+y<<endl;
    cout<<"for subtraction : "<<x-y<<endl;
    cout<<"for multiplication : "<<x*y<<endl;
    cout<<"for division : "<<x/y<<endl;
    cout<<"for modulo : "<<x%y<<endl;
    cout<<"for x++ : "<<x++<<endl;
    cout<<"for x-- : "<<x--<<endl;
    cout<<"for ++X : "<<++x<<endl;
    cout<<"for --x : "<<--x<<endl;

}